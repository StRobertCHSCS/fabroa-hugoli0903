# Receive the temperature in Celsius from the user
celsius = float(input("Enter the temperature in Celsius: "))
# Compute Farenheit from Celsius
farenheit = ((9/5*celsius) + 32)
# Output the temperature in Farenheit
print("The temperature in Farenheit from Celsius is:", farenheit)
